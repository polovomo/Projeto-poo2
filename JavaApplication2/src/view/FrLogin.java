    private boolean verificarCampo(){
        if(editUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Usuario em branco");
            return false;
        }
        if(new String(editSenha.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "senha em branco");
            return false;
        }
         return false;
    }
    
    
    
    private void logar(){
     if(!verificarcampos()){
         return;
    }
        
    //ler campos
       String usuario = editUsuario.getText();
       String senha = new String (editSenha.getPassword());
       
       
       //guardar dados
        UsuarioController controller = new UsuarioController();
       
       
    if (controller.autenticardo(usuario, senha)){
    //entra sistema
    FrMenu telaMenu = new FrMenu();
    
    telaMenu.setVisible(true);
   
    this.setVisible(false);
    
    JOptionPane.showMessageDialog(rootPane, "Logado");
    }else {
    //mensagem de suario nao encontrsdo
     JOptionPane.showMessageDialog(rootPane, "Usuario nao encontrado");
    }
       
       
       //consultar dados
    }
    
