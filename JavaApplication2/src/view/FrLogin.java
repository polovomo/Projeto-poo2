 private boolean verificarcampos() {
    if (editUsuario.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Usuário em branco");
        return false;
    }

    if (new String(editSenha.getPassword()).trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Senha em branco");
        return false;
    }

    return true;
 }
